function randomEmoji(array, length = 1){
  return [...shuffle(array)].slice(0, length)
}
function* addRandomSkinTone(chars){
  var skinTones = []
  for(const char of chars){
    if(skinTones.length == 0){
      skinTones = [...shuffle(["🏻","🏼","🏽","🏾","🏿"])]
    }
    const skinTone = skinTones.splice(0,1)
    yield char + skinTone
  }
}
